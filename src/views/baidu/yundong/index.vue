    run(map){
      var _that=this
      let startPionts=new BMap.Point(this.markerarr[0].lng,this.markerarr[0].lat)
      let endPionts=new BMap.Point(this.markerarr[this.markerarr.length-1].lng,this.markerarr[this.markerarr.length-1].lat)
      var drv = new BMap.DrivingRoute(startPionts,{
          onSearchComplete: function(res) {
            // console.log(res.getPlan(0).getRoute(0).getPath())
            if (drv.getStatus() == BMAP_STATUS_SUCCESS) {
                var plan = res.getPlan(0);
                var arrPois =[];
                for(var j=0;j<plan.getNumRoutes();j++){
                    var route = plan.getRoute(j);
                    arrPois= arrPois.concat(route.getPath());
                }
                map.addOverlay(new BMap.Polyline(arrPois, {strokeColor: 'red'}));
                map.setViewport(arrPois);
                _that.lushu = new BMapLib.LuShu(map,arrPois,{
                defaultContent:"",//""
                autoView:true,//
                icon  : new BMap.Icon('/image/ic_deiving.png', new BMap.Size(24,26),{anchor : new BMap.Size(12, 13)}),
                speed: 4500,
                enableRotation:true,//marker
                landmarkPois: [
                   {lng:116.314782,lat:39.913508,html:'',pauseTime:2},
                   {lng:116.315391,lat:39.964429,html:'<div><img src="//map.baidu.com/img/logo-map.gif"/></div>',pauseTime:3},
                   {lng:116.381476,lat:39.974073,html:'',pauseTime:2}
                ]});
            }
          }
    });
    var start=startPionts;
    var end=endPionts;
     drv.search(start, end);
    setTimeout(()=>{
      _that.lushu.start();
    },300)
    }
  }
