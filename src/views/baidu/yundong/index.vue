    startPiont(map){
      let startPionts=new BMap.Point(this.markerarr[0].lng,this.markerarr[0].lat)
      let startIcon=new BMap.Icon('/image/ic_deiving.png',new BMap.Size(24, 32),{anchor: new BMap.Size(12, 10)})
      this.startMarker=new BMap.Marker(startPionts,{icon:startIcon})
